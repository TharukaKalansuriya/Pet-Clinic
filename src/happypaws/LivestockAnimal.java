  int b = Integer.parseInt(Age);
  float c =Float.parseFloat(Weight);
  
  connect.livestockData(a,b,c,Sex,Tgs,Spc);
  
   //popup
   
  JOptionPane.showMessageDialog(this,"INSERTED SUCCESSFULLY!!");
        
