 connect.dbsconnection();
  String id= aniid.getText();
  String Weight = weight.getText();
  String Age= age.getText();
  String Tgs= tags.getText();
  String Spc = species.getText();
  
  
   String Sex="";
                   if(male.isSelected()){
                       Sex="Male";}
                   else{Sex="Female";
                      
                   }

  
  
